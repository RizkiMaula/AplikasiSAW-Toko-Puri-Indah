    public void kdBob() {
        try{
            String sql = "SELECT MAX(right(kd_bob, 2)) AS kode_masuk FROM bobot";
            Statement stmt = conn.createStatement();
            ResultSet rs = stmt.executeQuery(sql);
            
            while(rs.next()) {
                if(rs.first()==false) {
                    kdBobot.setText("Bob-001");
                } else {
                    rs.last();
                    int kd_masuk = rs.getInt(1) + 1;
                    String nomor = String.valueOf(kd_masuk);
                    int kdMasuk = nomor.length();
                    
                    for (int i = 0; i<3-kdMasuk; i++) {
                        nomor = "0" + nomor;
                    }
                    kdBobot.setText("Bob-" + nomor);
                }
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Koneksi Gagal " +e.getMessage());
        }
    }
    
    
