    protected void dataTable() {
        DefaultTableModel tabelDataPengeluaran = new DefaultTableModel();
        tabelDataPengeluaran.addColumn("No");
        tabelDataPengeluaran.addColumn("Kode Bobot");
        tabelDataPengeluaran.addColumn("Nama Kandidat");
        tabelDataPengeluaran.addColumn("Nilai");
        
        try {
            int no = 1;
            String sql = "SELECT bobot.kd_bob, recruitment.nama_kandidat, bobot.vtotal "
                       + "FROM `bobot` "
                       + "INNER JOIN recruitment ON recruitment.kd_kan = bobot.kd_kan "
                       + "ORDER BY `bobot`.`vtotal` DESC ";
            Statement stmt = conn.createStatement();
            ResultSet rs = stmt.executeQuery(sql);
            
            while(rs.next()) {
                tabelDataPengeluaran.addRow(new Object[]{
                   no++,
                   rs.getString(1),
                   rs.getString(2),
                   rs.getString(3),
                });
            }
            tabelPengeluaran.setModel(tabelDataPengeluaran);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Koneksi Gagal a "+e.getMessage());
        }
    }
    
